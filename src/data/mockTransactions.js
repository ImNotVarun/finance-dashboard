/**
 * Fetches historical stock data from Yahoo Finance via a CORS proxy.
 * Note: Free proxies like 'corsproxy.io' are great for development.
 */
export const fetchYahooStockData = async (symbol) => {
    try {
        const now = Math.floor(Date.now() / 1000);
        const oneMonthAgo = now - (30 * 24 * 60 * 60);

        // Yahoo Finance Chart Endpoint
        const yahooUrl = `https://query1.finance.yahoo.com/v8/finance/chart/${symbol}?period1=${oneMonthAgo}&period2=${now}&interval=1d`;

        // Using a CORS proxy to bypass browser security restrictions
        const proxyUrl = `https://corsproxy.io/?${encodeURIComponent(yahooUrl)}`;

        const response = await fetch(proxyUrl);
        if (!response.ok) throw new Error('Network response was not ok');

        const data = await response.json();
        const result = data.chart.result[0];

        if (result && result.timestamp) {
            const timestamps = result.timestamp;
            const prices = result.indicators.quote[0].close;

            return timestamps.map((ts, i) => ({
                date: new Date(ts * 1000).toISOString().split('T')[0],
                value: prices[i] ? parseFloat(prices[i].toFixed(2)) : null
            })).filter(item => item.value !== null);
        }

        return [];
    } catch (error) {
        console.error(`Yahoo Finance Error (${symbol}):`, error);
        return [];
    }
};